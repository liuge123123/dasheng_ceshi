    public static String newId()
    {
        Date now = new Date();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyyMMddHHmmss");
        String id = sdf.format(now);
        UUID suuid = UUID.randomUUID();
        String guid = suuid.toString().replace("-", "");
        id += guid.substring(0, 18);
        return id;
    }

